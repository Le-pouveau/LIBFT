int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int	counter;

	counter = 0;
	while ((*s1 || *s2) && (counter < n))
	{
		if ((*s1 > *s2) || (*s1 < *s2))
			return (*s1 - *s2);
		s1++;
		s2++;
		counter++;
	}
	return (0);
}
